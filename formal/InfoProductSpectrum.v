(*  RDL_ProductSpectrum.v                                                  *)
(*  THE SPECTRAL STRUCTURE OF PRODUCT GRAPHS IS GENERATED BY THE           *)
(*  FACTORS — the Kronecker-sum step of the n-dimensional readout          *)
(*  ladder, entirely over Q (no reals, no limits, no spectral theory:      *)
(*  eigen-structure is carried extensionally by exact Rayleigh pairs,      *)
(*  as in RDL_SpectralCeiling.v).                                          *)
(*                                                                         *)
(*  The Cartesian product of graphs G (nodes 0..nG-1, edges EG) and        *)
(*  H (nodes 0..nH-1, edges EH) has node set {(i,j)} and one edge per      *)
(*  pair (EG-edge, H-node) plus one per pair (G-node, EH-edge).  Its       *)
(*  quadratic form is therefore the edge sum GROUPED BY DIRECTION —        *)
(*  every product edge lies in exactly one group — which is how pgform     *)
(*  below is written; fields on the product are two-index functions.       *)
(*                                                                         *)
(*  CLAIMED HERE (candidate for coqc 8.18.0, axiom-free target):           *)
(*    form_separates      pgform(x (x) y)                                  *)
(*                          == gd_G(x) * |y|^2 + |x|^2 * gd_H(y)   EXACT   *)
(*    norm_separates      pnorm(x (x) y) == |x|^2 * |y|^2          EXACT   *)
(*    rayleigh_additive   exact Rayleigh pairs COMPOSE:                    *)
(*                          (lam, x) on G and (mu, y) on H give            *)
(*                          (lam + mu, x (x) y) on the product —           *)
(*                        the Kronecker-sum law, with no spectral          *)
(*                        theorem invoked                                  *)
(*    pnorm_pos           the composed pair is nondegenerate whenever      *)
(*                        the factors are                                  *)
(*    ceiling_additive    the degree ceilings of RDL_SpectralCeiling.v     *)
(*                        compose additively across factors                *)
(*    gd_const / rayleigh_lifts                                            *)
(*                        the constant field is a null direction of any    *)
(*                        factor, so every exact Rayleigh pair of one      *)
(*                        factor LIFTS UNCHANGED to the product            *)
(*                        (composing with RDL_GraphGrowth.ramp_dilution    *)
(*                        this hands the slow-mode witness to every        *)
(*                        higher-dimensional grid for free)                *)
(*                                                                         *)
(*  Consequence for the ladder: the multi-dimensional spectral             *)
(*  structure of grid graphs is GENERATED, exactly and over Q, by the      *)
(*  one-dimensional factors; only the one-dimensional readout limit        *)
(*  (already available at its own tier) remains per axis.                  *)
(*                                                                         *)
(*  HONESTLY NOT CLAIMED: the CONVERSE spectral statement (that every      *)
(*  Rayleigh value of the product arises as such a sum — classically       *)
(*  true, but it needs a full spectral decomposition we do not invoke);    *)
(*  and any continuum limit (that is the next rung, at Tier-2).            *)
(*                                                                         *)
(*  Pre-verified with exact rational arithmetic (300 random factor         *)
